
public class BIT4 {

    public static void main(String[] args) {

        // << BINARY LEFT SHIFT OPERATOR 

        /*
            a << b => WHOLE BINARY NUMBER SHIFT BY b TOWARDS THE LEFT 
            5    :    0 0 1 0 1 
                          |___|

            5<<2 :    1 0 1 0 0 => 20
                      |___|


            a << b = a*(2^b) 

            
         */
        System.out.println(5 << 2);

    }
}
