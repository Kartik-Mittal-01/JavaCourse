
public class BIT3 {

    public static void main(String[] args) {

        // ~ operator 
        /*
            MST = 1 => NUMBER IS NEGATIVE 
            MST = 0 => NUMBER IS POSTIVE 
            ~1 -> 0
            ~0 -> 1
            
         */
        System.out.println(~0);
        System.out.println(~1);
        System.out.println(~5);  // -> -6

    }
}
