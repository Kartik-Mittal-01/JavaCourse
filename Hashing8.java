
import java.util.LinkedHashSet;

public class Hashing8 {

    public static void main(String[] args) {

        // LINKED HASH SETS 
        // ENTRIES ARE INSETION ORDERED 
        LinkedHashSet<Integer> set = new LinkedHashSet<>();
        set.add(1);
        set.add(2);
        set.add(3);
        set.add(4);
        set.add(5);

        System.out.println(set);

    }
}
