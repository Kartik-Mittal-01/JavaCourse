
public class BIT5 {

    public static void main(String[] args) {

        // >> BINARY RIGHT SHIFT OPERATOR 

        /*
            a >> b => WHOLE BINARY NUMBER SHIFT BY b TOWARDS THE RIGHT 
            5    :    0 0 1 0 1 
                          |___|

            5<<2 :    0 0 0 0 1 => 1
                              |__


            a >> b = a/(2^b) 

            
         */
        System.out.println(5 >> 2);

    }
}
